#include "mini_rt.h"

t_vec	*reflect(t_vec *dest, t_vec *v1, t_vec *v2)
{
	v_mul(v2, v2, 2 * v_dot(v1, v2));
	v_sub(dest, v1, v2);
	return (dest);
}
